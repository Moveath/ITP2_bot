
# 1. СҰРАҚ ГЕНЕРАЦИЯСЫНА АРНАЛҒАН ПРОМПТ
def build_prompt(text: str, level: int, previous_context: str = "") -> str:
    level_desc = {
        1: "өте оңай, тек негізгі фактілерді сұра",
        2: "орташа, түсінікті тексеру",
        3: "қиын, аналитикалық ойлауды қажет етеді",
        4: "өте қиын, бірнеше концепцияны салыстыру",
        5: "эксперт деңгей, терең білімді талап етеді"
    }
    difficulty = level_desc.get(level, "орташа")

    prompt = f"""Сен осы мәтін бойынша MCQ тест сұрақтарын жаса.
Сұрақ қиындығы: {difficulty} (деңгей {level}).
Әр сұрақтың 4 нұсқасы болсын, тек біреуі дұрыс. Дұрыс жауаптың индексін 0-3 аралығында көрсет.
Қысқаша түсіндірме жаз ("explanation" өрісі).

Жауапты тек JSON форматында қайтар, былай:
{{
  "questions": [
    {{
      "text": "Сұрақ мәтіні",
      "options": ["A) ...", "B) ...", "C) ...", "D) ..."],
      "correct": 0,
      "explanation": "..."
    }}
  ]
}}
"""
    if previous_context:
        prompt += f"\n\nНазар аудар: пайдаланушы бұған дейін мынадай жауаптар берген, оның деңгейіне сай сұрақтар құрастыр:\n{previous_context}"

    prompt += f"\n\nМәтін:\n{text}"
    return prompt


